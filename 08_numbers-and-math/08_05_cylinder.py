# Write the necessary code calculate the volume and surface area
# of a cylinder with a radius of 3.14 and a height of 5.
# Print out the result.

pi = 3.14159
radius = 3.14
height = 5
volume = pi * (radius ** 2) * height
surface_area = 2 * pi * radius * (height + radius)
print(volume)
print(surface_area)